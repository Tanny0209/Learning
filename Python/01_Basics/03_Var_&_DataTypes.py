# DataTypes in Python

# 1. int store whole numbers
a=10
print(type(a)) #This will print the data type of variable a

# 2. float store decimal numbers
b=10.5
print(type(b)) 

# 3. str store text
c="Hello, World!"
print(type(c)) 

# 4. bool store boolean values
d=True
print(type(d)) 

#5. none store the absence of a value
e=None
